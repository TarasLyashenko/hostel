package cats;

public class Cat
{
    public String name;
    public float age;
    public String breed;
    public float weight;
    public String color;
    public String character;
    public int price;
    public String gender;

    public Cat(String names, float ages, String breeds,
               float weights, String colors, String characters,
               int prices, String genders)
    {
        name = names;
        age = ages;
        breed = breeds;
        weight = weights;
        color = colors;
        character = characters;
        price = prices;
        gender = genders;
    }

}
