package myFight.model;

public class Fighter
{
    public String race;
    public String weapon;
    public String health;
    public String mana;
    public String ultimateAbility;
    public String spell;


    public Fighter(String race, String weapon, String health, String mana, String ultimateAbility, String spell)
    {

        this.race = race;
        this.weapon = weapon;
        this.health = health;
        this.mana = mana;
        this.spell = spell;
        this.ultimateAbility = ultimateAbility;
        this.spell = spell;
    }
}
